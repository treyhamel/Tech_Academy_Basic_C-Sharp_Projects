using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace Page111_Task
{
    class Operations
    {
        public static int Square(int numOne)
        {
            int result = numOne * numOne;
            return result;
        }
        public static int Divide(int numOne)
        {
            int result = numOne / 2;
            return result;
        }
        public static int Subtract(int numOne)
        {
            int result = numOne - 2;
            return result;
        }
    }
}
